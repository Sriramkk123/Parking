package com.sriram.parking.users;

//Each member will have this class as a attribute to store details of the user
public class Person {
	  private String name;
	  private String email;
	  private String phone;
	  
	  public Person(String name, String email, String phone)
	  {
		  this.name = name;
		  this.email = email;
		  this.phone = phone;
	  }
}
